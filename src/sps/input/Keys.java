    public static Keys fromName(String s) {
        for (Keys key : values()) {
            if (key.name().equalsIgnoreCase(s)) {
                return key;
            }
        }
        return null;
    }

    public static Keys fromCode(int keyCode) {
        if (keyCode == 16) {
            return NUM_9;
        }
        for (Keys key : values()) {
            if (key.getKeyCode() == keyCode) {
                return key;
            }
        }
        return null;
    }

